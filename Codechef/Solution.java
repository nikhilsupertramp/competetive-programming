import java.io.*;
import java.util.*;
class Solution{
    public static void main(String[] args)throws Exception {
        Scanner sc = new Scanner(System.in);
        for(int tc = sc.nextInt(); tc > 0; tc--)
        {
            long x = sc.nextLong();
            long y = sc.nextLong();
            long l = sc.nextLong();
            long r = sc.nextLong();
            long max = Integer.MIN_VALUE;
            long ans = 0;
            for(long i = l; i <= r; i++)
            {
                long sol = (x & i) * (y & i);
                if(sol > max)
                {
                    max = sol;
                    ans = i;
                }
            }
            System.out.println(ans + " " + max);
        }
    }

}
